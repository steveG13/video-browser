<template>
  <div class="col-md-8" v-if="video">
    <div class="embed-responsive embed-responsive-16by9">
      <iframe class="embed-responsive-item" v-bind:src="videoUrl" />
    </div>
    <div class="details">
      <h4>{{ video.snippet.title }}</h4>
      <p>{{ video.snippet.description }}</p>
    </div>
  </div>
</template>

<script>
export default {
  name: 'videoDetail',
  props: {
    video: Array
  },
  computed: {
    videoUrl () { //compute the video URL
      const videoId = this.video.id.videoId;
      return `https://www.youtube.com/embed/${videoId}`; //inline the URL with the ID variable
    }
  }
}
</script>

<style scoped>
.details {
  margin-top: 10px;
  padding: 10px;
  border: 1px solid #ddd;
  border-radius: 4px;
}
</style>